package application

////////////////////////////////////////////////////////////////////////////////

// MainLooper 【inject:"#main-looper"】
type MainLooper interface {
	RunMain() error
}

////////////////////////////////////////////////////////////////////////////////

func runMainLooper(c Context) error {
	const selector = "#main-looper"
	o1, err := c.GetComponent(selector)
	if err != nil {
		return err
	}
	o2 := o1.(MainLooper)
	return o2.RunMain()
}

